interface TranslationContextType {
  locale: string;
  setLocale: (loc: string) => void;
  t: (key: string) => string;
}

const TranslationContext = createContext<TranslationContextType | undefined>(undefined);

export function TranslationProvider({ children }: { children: React.ReactNode }) {
  const [locale, setLocaleState] = useState<string>('es');

  useEffect(() => {
    const stored = window?.localStorage?.getItem('devhub-locale');
    if (stored) {
      setLocaleState(stored);
    }
  }, []);

  const t = (key: string): string => {
    return messages[locale]?.[key] ?? key;
  };

  const setLocale = (loc: string) => {
    window?.localStorage?.setItem('devhub-locale', loc);
    setLocaleState(loc);
  };

  return React.createElement(TranslationContext.Provider, { value: { locale, setLocale, t } }, children);
}

export function useTranslation() {
  const context = useContext(TranslationContext);
  if (context === undefined) {
    return { 
      t: (key: string) => key, 
      locale: 'es', 
      setLocale: () => {} 
    };
  }
  return context;
}
